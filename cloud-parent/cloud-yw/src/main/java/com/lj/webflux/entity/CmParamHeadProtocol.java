package com.lj.webflux.entity;

public class CmParamHeadProtocol {
    String code;
    String name;
    String paramCodes;
    String paramkv;
    String dict;
    String type;
    String unit;
    String trainCate;
    String partCode;
    String sysName;
    String remark;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getParamCodes() {
        return paramCodes;
    }

    public void setParamCodes(String paramCodes) {
        this.paramCodes = paramCodes;
    }

    public String getParamkv() {
        return paramkv;
    }

    public void setParamkv(String paramkv) {
        this.paramkv = paramkv;
    }

    public String getDict() {
        return dict;
    }

    public void setDict(String dict) {
        this.dict = dict;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getTrainCate() {
        return trainCate;
    }

    public void setTrainCate(String trainCate) {
        this.trainCate = trainCate;
    }

    public String getPartCode() {
        return partCode;
    }

    public void setPartCode(String partCode) {
        this.partCode = partCode;
    }

    public String getSysName() {
        return sysName;
    }

    public void setSysName(String sysName) {
        this.sysName = sysName;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

}
